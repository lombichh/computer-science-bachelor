package beans;

public class IotInfo {
	private int temp;
	private int averageTemp;
	
	public IotInfo() {
	}

	public int getTemp() {
		return temp;
	}

	public void setTemp(int temp) {
		this.temp = temp;
	}

	public int getAverageTemp() {
		return averageTemp;
	}

	public void setAverageTemp(int averageTemp) {
		this.averageTemp = averageTemp;
	}
	
}